CREATE TABLE IF NOT EXISTS TB_USUARIO (
    codigo_USUARIO INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    EMAIL VARCHAR(500) NOT NULL,
    SENHA VARCHAR(500) NOT NULL,
    PONTUACAO INTEGER NOT NULL DEFAULT 0,
    CONSTRAINT uk_usuario_email UNIQUE (EMAIL)
);

CREATE TABLE IF NOT EXISTS TB_PERGUNTA (
    codigo_PERGUNTA INTEGER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PERGUNTA VARCHAR(500) NOT NULL,
    Q1 VARCHAR(500) NOT NULL,
    Q2 VARCHAR(500) NOT NULL,
    Q3 VARCHAR(500) NOT NULL,
    Q4 VARCHAR(500) NOT NULL,
    EXPLICACAO VARCHAR(500) NOT NULL,
    INDICE_RESPOSTA INTEGER NOT NULL
);

